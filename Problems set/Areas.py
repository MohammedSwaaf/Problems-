def Triangle(x,y):
    t=0.5*x*y       #Area for Triangle
    print(t)
def rectangle(a,b):
    r=a*b       #Area for rectangle
    print(r)
def Circle(r):
    c=3.14*(r**2)   #Area for circle
    print(c)
def square(z):      #Area for square
    s=z*4
    print(s)

#calling
Triangle(10,7)
rectangle(10,7)
Circle(10)
square(10)

